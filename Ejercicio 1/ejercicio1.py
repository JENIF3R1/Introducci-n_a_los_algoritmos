# EJERCICIO 1: Un estudiante está cursando 5 materias, tiene la nota de cada
# materia y quiere saber cuál es su promedio.

numero1 = int(input("Ingresar nota de materia 1: "))
numero2 = int(input("Ingresar nota de materia 2: "))
numero3 = int(input("Ingresar nota de materia 3: "))
numero4 = int(input("Ingresar nota de materia 4: "))
numero5 = int(input("Ingresar nota de materia 5: "))

suma = numero1 + numero2 + numero3 + numero4 + numero5

promedio = suma / 5

print("El promedio de las notas de las 5 materias es:", promedio)
